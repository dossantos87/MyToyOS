#define false 0

typedef char      _i8;
typedef short     _i16;
typedef int       _i32;
typedef long long _i64;

typedef unsigned char       _u8;
typedef unsigned short      _u16;
typedef unsigned int        _u32;
typedef unsigned long long  _u64;

#endif

